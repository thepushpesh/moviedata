export const trendingMovies = movies.filter((movie) => movie.isTrending);
export const popularMovies = movies.filter((movie) => movie.isPopular);

export function getMovieById(id) {
  return movies.find((movie) => movie.id === id);
}

export function searchMovies(query = "", page = 1, pageSize = 8) {
  const normalizedQuery = query.trim().toLowerCase();
  const filtered = normalizedQuery
    ? movies.filter(
        (movie) =>
          movie.title.toLowerCase().includes(normalizedQuery) ||
          movie.genres.join(" ").toLowerCase().includes(normalizedQuery)
      )
    : movies;

  const safePage = Number.isNaN(page) || page < 1 ? 1 : page;
  const start = (safePage - 1) * pageSize;
  const end = start + pageSize;
  const totalPages = Math.max(1, Math.ceil(filtered.length / pageSize));

  return {
    items: filtered.slice(start, end),
    total: filtered.length,
    totalPages,
    page: safePage,
    hasNextPage: safePage < totalPages,
    hasPrevPage: safePage > 1,
  };
}
